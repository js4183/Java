public class EX08for문 {

	public static void main(String[] args) {
		for (int i = 21; i <= 57; i++) {
			System.out.print(i + " ");
		}
		System.out.println();
		for (int j = 96; j >= 53; j--) {
			System.out.print(j + " ");
		}
		System.out.println();
		for (int k = 21; k <= 57; k ++) {
			if(k%2==1)
			System.out.print(k + " ");
		}

	}

}
